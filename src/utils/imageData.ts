export function randomPick(type: string, quality = 'low', num = 20, dir = '', theme = '') {
  let canPick = dir ? images.filter(i => i.startsWith(dir)) : images
  canPick = theme ? canPick.filter(i => i.includes(theme)) : canPick
  const picked = new Set<string>()
  while (picked.size < num && picked.size < canPick.length) {
    const randomIndex = Math.floor(Math.random() * canPick.length)
    picked.add(canPick[randomIndex])
  }
  return Array.from(picked).map(i => `/images_${quality}/${i}${type}`)
}
